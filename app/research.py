# --- Search providers -------------------------------------------------------
#
# Provider implementations live in ``app/sources/``. Round 1 of
# ``run_research`` iterates every registered provider via
# ``_round1_search_all_providers`` (Serper, Tavily, arXiv, HN, Wikipedia,
# plus any newly registered providers). Rounds 2 and 3 stay Serper-only
# because they use targeted LLM-generated queries — ``_search_serper``
# below is the shared entrypoint for those rounds.

async def _search_serper(
    client: httpx.AsyncClient, query: str, num: int = 8
) -> list[dict]:
    """Thin shim over ``app.sources.SerperProvider`` for rounds 2/3."""
    from app.sources import SerperProvider
    return await SerperProvider(client).search(query, num=num)


async def _round1_search_all_providers(
    client: httpx.AsyncClient, query: str, per_provider: int = 5,
) -> list[dict]:
    """Run round 1 against every registered provider in parallel.

    Each provider:
      - returns at most ``per_provider`` results
      - has ~15s to complete via httpx defaults; failures are caught and logged
      - graceful degradation: missing API keys → empty list, no exception

    Returns the merged result list (caller still dedups + tier-scores).
    """
    from app.sources import get_provider_classes

    provider_classes = get_provider_classes()
    if not provider_classes:
        return []

    async def _run_one(provider_cls):
        try:
            provider = provider_cls(client)
            results = await provider.search(query, num=per_provider)
            return (provider_cls.name, results or [])
        except Exception as exc:
            logger.warning(
                "Round 1 provider %s failed: %s", provider_cls.name, exc,
            )
            return (provider_cls.name, [])

    pairs = await asyncio.gather(*[_run_one(p) for p in provider_classes])
    merged: list[dict] = []
    for name, results in pairs:
        if results:
            logger.info("Round 1 [%s]: %d results", name, len(results))
            merged.extend(results)
    return merged


# --- LLM helper (delegates to app.llm) ------------------------------------

async def _chat(
    client: httpx.AsyncClient,
    system_msg: str,
    user_msg: str,
    max_tokens: int = 4000,
    temperature: float = 0.2,
    *,
    model: str | None = None,
    provider: str | None = None,
) -> str:
    """Send a chat completion via the configured LLM provider.

    Optional ``model`` and ``provider`` kwargs override the global default
    for a single call. Used to route synthesis through the per-KB
    ``kb_settings.synthesis_provider`` / ``synthesis_model``.
    """
    return await llm_chat(
        system_msg, user_msg, max_tokens, temperature,
        client=client, model=model, provider=provider,
    )


# --- Sub-question / verification generators ---------------------------------

async def _generate_sub_questions(
    client: httpx.AsyncClient, topic: str, round1_summaries: str
) -> list[str]:
    """Generate follow-up research sub-questions using chain-of-thought evaluation."""
    prompt = (
        f'You are evaluating search results about "{topic}" to decide what to research next.\n\n'
        f"Step 1 - EVALUATE what you have: Review the sources below. Think about:\n"
        f"  - What aspects of this topic are well-covered by authoritative sources?\n"
        f"  - What aspects are missing, vague, or only covered by low-quality sources?\n"
        f"  - Are there obvious follow-up questions a reader would have?\n\n"
        f"Step 2 - DECIDE what's needed: Based on your evaluation, generate 3-4 specific "
        f"sub-questions that would fill the most important gaps. Focus on:\n"
        f"- Aspects no source covered well (biggest gaps first)\n"
        f"- Practical details that are vague or missing\n"
        f"- Comparisons or alternatives readers would want to know\n"
        f"- Recent developments the initial search may have missed\n\n"
        f"Return ONLY the questions, one per line, no numbering or bullets. "
        f"Put the most important gap first.\n\n"
        f"Initial results:\n{round1_summaries}"
    )
    response = await _chat(
        client,
        system_msg="You are a research agent evaluating source coverage. Think step by step about what's missing, then generate targeted sub-questions to fill the biggest gaps. Return only the questions, one per line.",
        user_msg=prompt,
        max_tokens=600,
        temperature=0.3,
    )
    return [
        q.strip().lstrip("0123456789.-) ")
        for q in response.strip().split("\n")
        if q.strip() and len(q.strip()) > 10
    ][:4]


async def _generate_verification_queries(
    client: httpx.AsyncClient, topic: str, sources_text: str
) -> list[str]:
    """Identify claims needing verification using chain-of-thought reasoning."""
    prompt = (
        f'You are fact-checking research about "{topic}" before it gets published.\n\n'
        f"Step 1 - SCAN for risky claims: Review the sources and identify claims that:\n"
        f"  - Are only supported by a single source (especially Tier 3)\n"
        f"  - Contain specific numbers, versions, prices, or dates that could be outdated\n"
        f"  - Make strong comparative claims (faster, better, replaces X)\n"
        f"  - Contradict another source in the results\n"
        f"  - Would be embarrassing to publish if wrong\n\n"
        f"Step 2 - PRIORITIZE: Pick the 2-3 claims that matter most to get right. "
        f"A wrong version number in a recommendation is worse than a wrong founding date.\n\n"
        f"Step 3 - Generate a targeted search query for each claim that would find "
        f"an independent source to confirm or deny it.\n\n"
        f"Return ONLY the search queries, one per line.\n\n{sources_text}"
    )
    response = await _chat(
        client,
        system_msg="You are a fact-checker. Think step by step: scan for risky claims, prioritize the ones that matter most, then generate verification queries. Return only search queries, one per line.",
        user_msg=prompt,
        max_tokens=500,
        temperature=0.2,
    )
    return [
        q.strip().lstrip("0123456789.-) ")
        for q in response.strip().split("\n")
        if q.strip() and len(q.strip()) > 10
    ][:3]


# --- Source scoring & dedup -------------------------------------------------

def _score_source_tier(url: str) -> int:
    """Score a URL's authority tier. Returns 1 (highest), 2, or 3 (lowest)."""
    if not url:
        return 3
    url_lower = url.lower()

    try:
        hostname = urlparse(url_lower).hostname or ""
    except Exception:
        hostname = ""

    tld = hostname.rsplit(".", 1)[-1] if "." in hostname else ""
    if tld in TIER1_DOMAINS:
        return 1

    for pattern in TIER1_SUBSTRINGS:
        if pattern in url_lower:
            return 1
    for pattern in TIER2_SUBSTRINGS:
        if pattern in url_lower:
            return 2

    return 3


def _tier_label(tier: int) -> str:
    return {1: "Tier 1 - Authoritative", 2: "Tier 2 - Reputable", 3: "Tier 3 - General"}[tier]


# Self-citation lint — strip ``[label](url)`` links whose URL was never in
# the source set the model was given. The regex catches every inline link
# and ignores reference-style links + bare URLs in code fences.
_MARKDOWN_LINK_RE = re.compile(r"\[([^\]]+)\]\((https?://[^)\s]+)\)")


def _normalize_url_for_match(url: str) -> str:
    """Strip protocol + trailing slash + leading www. for fuzzy URL matching."""
    if not url:
        return ""
    u = url.strip().rstrip("/").lower()
    u = u.replace("https://", "").replace("http://", "")
    if u.startswith("www."):
        u = u[4:]
    return u


def _lint_citations(
    markdown: str, source_urls: list[str],
) -> tuple[str, list[str]]:
    """Strip hallucinated citation links from synthesis output.

    Walks every ``[label](url)`` in *markdown*. Any URL not present in the
    *source_urls* set (matched after protocol/www normalization) gets its
    link stripped — the label text is preserved so the prose still reads
    naturally, just without the fake link. Returns the cleaned markdown
    plus the list of stripped URLs (for logging onto the job record).
    """
    if not markdown:
        return markdown, []

    allowed = {_normalize_url_for_match(u) for u in source_urls if u}
    stripped: list[str] = []

    def _replace(match: re.Match) -> str:
        label = match.group(1)
        url = match.group(2)
        if _normalize_url_for_match(url) in allowed:
            return match.group(0)  # keep as-is
        stripped.append(url)
        return label  # drop the link, keep the label

    cleaned = _MARKDOWN_LINK_RE.sub(_replace, markdown)
    return cleaned, stripped


# Adaptive per-tier content cap for the synthesis prompt. Tier 1
# (authoritative docs, official sources, academic) gets the most room
# because we paid the most to fetch it. Tier 3 gets the least so junk
# forums can't crowd out the high-signal material.
_TIER_CONTENT_CAP = {1: 8000, 2: 3000, 3: 1000}


def _truncate_for_tier(content: str, tier: int) -> str:
    """Cap a source's content length based on its authority tier."""
    if not content:
        return ""
    cap = _TIER_CONTENT_CAP.get(tier, 1000)
    if len(content) <= cap:
        return content
    return content[:cap].rstrip() + " ..."


def _deduplicate_results(results: list[dict]) -> list[dict]:
    """Remove duplicate results by URL and near-duplicate content."""
    # Pass 1: URL dedup
    seen_urls: set[str] = set()
    url_deduped: list[dict] = []
    for r in results:
        url = r.get("url", "").strip().rstrip("/")
        if not url:
            url_deduped.append(r)
            continue
        norm_url = url.lower().replace("https://", "").replace("http://", "").lstrip("www.")
        if norm_url in seen_urls:
            continue
        seen_urls.add(norm_url)
        url_deduped.append(r)

    # Pass 2: Content near-duplicate detection
    def _normalize_text(text: str) -> str:
        return re.sub(r"\s+", " ", text.lower().strip())[:300]

    final: list[dict] = []
    seen_content: list[str] = []
    for r in url_deduped:
        norm = _normalize_text(r.get("content", ""))
        if len(norm) < 20:
            final.append(r)
            continue
        is_dup = False
        for existing in seen_content:
            shorter, longer = (norm, existing) if len(norm) <= len(existing) else (existing, norm)
            if shorter in longer:
                is_dup = True
                break
            words_a = set(norm.split())
            words_b = set(existing.split())
            if words_a and words_b:
                overlap = len(words_a & words_b) / len(words_a | words_b)
                if overlap > 0.8:
                    is_dup = True
                    break
        if not is_dup:
            seen_content.append(norm)
            final.append(r)

    return final


