_CRITIQUE_SYSTEM = (
    "You are a rigorous fact-checking critic. You will be given a research draft "
    "and the source material that was used to write it. Your job is to:\n"
    "1. Check every major claim against the actual source text provided\n"
    "2. Flag claims where the draft states something the sources don't actually say "
    "(this is hallucination, even if the claim might be true from general knowledge)\n"
    "3. Flag claims where a citation [Source](url) exists but the cited source "
    "doesn't contain the specific fact being attributed to it\n"
    "4. Identify important topics the sources covered that the draft left out\n"
    "5. Check comparison tables for unsourced data (cells filled from general knowledge)\n"
    "You do NOT rewrite the draft. Return only a JSON report."
)


async def _critique_draft(
    client: httpx.AsyncClient,
    topic: str,
    draft: str,
    sources_text: str,
) -> list[dict]:
    """Run a critique pass over the synthesized draft.

    Returns a list of claim dicts:
        [{"text": str, "status": "supported"|"unsupported"|"missing", "note": str}, ...]

    Each claim has a status:
      - supported: the source material clearly backs the claim
      - unsupported: the claim isn't justified by the sources (likely hallucination)
      - missing: an important point about the topic that the draft omitted

    Returns ``[]`` if the LLM call or JSON parse fails — the caller treats
    that as "no critique available" and skips the Confidence Notes section.
    """
    user_msg = (
        f'Topic: "{topic}"\n\n'
        f"## Draft\n{draft[:8000]}\n\n"
        f"## Sources used\n{sources_text[:12000]}\n\n"
        f"Return ONLY a JSON object of the form:\n"
        f'{{"claims": [{{"text": "<claim>", "status": "supported|unsupported|missing", "note": "<short reason>"}}]}}\n\n'
        f"Rules:\n"
        f"- Up to 10 claims total.\n"
        f"- Prefer the most important / load-bearing claims, not trivia.\n"
        f"- Specifically check: are comparison table cells actually supported by sources?\n"
        f"- Specifically check: does each [Source](url) citation match what that source actually says?\n"
        f"- For 'unsupported' claims, the note should explain what's missing from the sources.\n"
        f"- For 'missing' claims, the note should explain why it matters and which source covered it.\n"
        f"- Output ONLY the JSON object, no markdown fences, no commentary."
    )
    try:
        raw = await _chat(
            client,
            system_msg=_CRITIQUE_SYSTEM,
            user_msg=user_msg,
            max_tokens=3000,
            temperature=0.1,
        )
    except Exception as exc:
        logger.warning("Critique pass failed: %s", exc)
        return []

    # Tolerant JSON parsing — strip optional markdown fence
    text = raw.strip()
    if text.startswith("```"):
        parts = text.split("\n", 1)
        if len(parts) == 2:
            text = parts[1]
        if text.endswith("```"):
            text = text[:-3]
        text = text.strip()
    # Find the outermost JSON object
    match = re.search(r"\{[\s\S]*\}", text)
    if not match:
        logger.warning("Critique pass returned no JSON object")
        return []
    try:
        obj = json.loads(match.group(0))
    except json.JSONDecodeError as exc:
        logger.warning("Critique JSON parse failed: %s", exc)
        return []

    raw_claims = obj.get("claims") or []
    if not isinstance(raw_claims, list):
        return []

    claims: list[dict] = []
    for c in raw_claims:
        if not isinstance(c, dict):
            continue
        text_v = (c.get("text") or "").strip()
        status_v = (c.get("status") or "").strip().lower()
        note_v = (c.get("note") or "").strip()
        if not text_v:
            continue
        if status_v not in ("supported", "unsupported", "missing"):
            status_v = "unsupported"
        claims.append({"text": text_v, "status": status_v, "note": note_v})
    return claims


def _render_confidence_notes(claims: list[dict]) -> str:
    """Render critique claims as an appended ## Confidence Notes section."""
    if not claims:
        return ""
    supported = [c for c in claims if c["status"] == "supported"]
    unsupported = [c for c in claims if c["status"] == "unsupported"]
    missing = [c for c in claims if c["status"] == "missing"]

    lines = ["", "", "## Confidence Notes", ""]
    lines.append(
        f"Critique pass: **{len(supported)} supported**, "
        f"**{len(unsupported)} unsupported**, "
        f"**{len(missing)} missing** claim(s)."
    )
    if unsupported:
        lines.append("")
        lines.append("**Claims the sources don't fully support:**")
        for c in unsupported:
            note = f" — _{c['note']}_" if c["note"] else ""
            lines.append(f"- {c['text']}{note}")
    if missing:
        lines.append("")
        lines.append("**Topics the sources cover that the draft omitted:**")
        for c in missing:
            note = f" — _{c['note']}_" if c["note"] else ""
            lines.append(f"- {c['text']}{note}")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Per-source extraction pre-pass
# ---------------------------------------------------------------------------

# Sources shorter than this aren't worth running through extraction; they're
# already a one-paragraph snippet from Serper/Tavily.
_EXTRACTION_MIN_CHARS = 1500
# Cap on how many sources we'll extract from in a single research run, to
# bound LLM cost. Tier-1 sources beyond this fall through to raw inclusion.
_EXTRACTION_MAX_SOURCES = 6


_FACT_EXTRACTION_SYSTEM = (
    "You extract concrete factual claims from a single source. "
    "Return a bulleted list — one claim per line, prefixed with `- `. "
    "Each bullet must be specific (numbers, version IDs, dates, named "
    "features) and self-contained. Use the exact wording from the source "
    "where possible. No prose, no headers, no commentary — bullets only."
)


async def _extract_facts_from_source(
    client: httpx.AsyncClient,
    topic: str,
    source: dict,
) -> str | None:
    """Run a cheap LLM extraction over one source's content.

    Returns a bullet list of facts as a string, or ``None`` if extraction
    failed (in which case the caller should fall back to the raw content).
    """
    content = source.get("content") or ""
    if len(content) < _EXTRACTION_MIN_CHARS:
        return None

    title = source.get("title") or "(untitled)"
    url = source.get("url") or "(no url)"
    # Cap input to keep the cheap-call budget in check.
    snippet = content[:8000]
    user_msg = (
        f'Topic of interest: "{topic}"\n\n'
        f"Source: {title}\nURL: {url}\n\n"
        f"Source content:\n{snippet}\n\n"
        f"Extract every factual claim from this source that is relevant "
        f"to the topic. Up to 12 bullets."
    )
    try:
        return await _chat(
            client,
            system_msg=_FACT_EXTRACTION_SYSTEM,
            user_msg=user_msg,
            max_tokens=600,
            temperature=0.1,
        )
    except Exception as exc:
        logger.warning("Fact extraction failed for %s: %s", url, exc)
        return None


async def _extract_facts_for_tier1_sources(
    client: httpx.AsyncClient,
    topic: str,
    results: list[dict],
) -> int:
    """Run extraction in parallel over the top Tier-1 sources.

    Mutates *results* in place: each extracted source gets its ``content``
    replaced by the bullet list and its dict tagged with
    ``extracted=True``. Returns the number of sources successfully
    extracted.
    """
    targets = [
        (i, r)
        for i, r in enumerate(results)
        if r.get("tier") == 1 and len(r.get("content") or "") >= _EXTRACTION_MIN_CHARS
    ][:_EXTRACTION_MAX_SOURCES]

    if not targets:
        return 0

    coros = [_extract_facts_from_source(client, topic, r) for _, r in targets]
    extracted_results = await asyncio.gather(*coros, return_exceptions=True)

    success = 0
    for (idx, source), result in zip(targets, extracted_results):
        if isinstance(result, Exception) or not result:
            continue
        result_text = result.strip()
        if not result_text:
            continue
        # Replace the raw blob with the bullet extraction; mark it so the
        # formatter can label it visibly in the prompt.
        results[idx] = {
            **source,
            "content": result_text,
            "extracted": True,
        }
        success += 1

    if success:
        logger.info(
            "Per-source extraction: %d/%d Tier-1 sources distilled",
            success, len(targets),
        )
    return success


def _format_sources_for_prompt(results: list[dict]) -> str:
    """Format search results with tier labels for the synthesis prompt.

    Each source's content is truncated according to its tier (8000 chars
    for Tier 1, 3000 for Tier 2, 1000 for Tier 3) so the expensive Tier-1
    deep-reads keep most of their material while Tier-3 forum noise can't
    crowd them out.
    """
    sections = []
    for r in results:
        content = r.get("content") or ""
        if not content:
            continue
        tier = r.get("tier", 3)
        label = _tier_label(tier)
        capped = _truncate_for_tier(content, tier)
        marker = " [EXTRACTED FACTS]" if r.get("extracted") else ""
        sections.append(
            f"[{label}]{marker} **{r['title']}** ({r['url']})\n{capped}"
        )
    return "\n\n".join(sections)


# --- Synthesis --------------------------------------------------------------

async def _synthesize(
    client: httpx.AsyncClient, topic: str, all_results: list[dict]
) -> tuple[str, list[dict]]:
    """Run the final synthesis step to produce a structured research document.

    Returns ``(article_markdown, critique_claims)``.

    Reads ``kb_settings.synthesis_provider`` / ``synthesis_model`` via the
    ``_current_kb`` ContextVar (set by ``run_research``) and passes them
    through to ``llm_chat``. NULL settings fall back to the global
    ``LLM_PROVIDER`` env var. No hard-coded model choice — the user picks
    per KB through the admin UI.

    After the draft, runs a critique pass that returns structured JSON
    about which claims are supported / unsupported / missing. The critique
    is appended to the article as a ``## Confidence Notes`` section AND
    returned to the caller so it can be persisted to ``article_claims``.
    """
    # Cheap parallel extraction over the top Tier-1 sources. Replaces raw
    # blobs with verbatim bullet facts so the synthesis pass consumes
    # signal-dense input rather than rambling page content.
    try:
        await _extract_facts_for_tier1_sources(client, topic, all_results)
    except Exception as exc:
        logger.warning("Per-source extraction pass failed: %s", exc)

    sources_text = _format_sources_for_prompt(all_results)

    # Per-KB synthesis provider/model + persona lookup. All three come
    # from kb_settings; NULL values fall back to the global defaults.
    synth_provider: str | None = None
    synth_model: str | None = None
    persona: str | None = None
    kb_name = _current_kb.get()
    if kb_name:
        try:
            kb_cfg = await db.get_kb_settings(kb_name)
            if kb_cfg:
                synth_provider = kb_cfg.get("synthesis_provider") or None
                synth_model = kb_cfg.get("synthesis_model") or None
                persona = kb_cfg.get("persona") or None
        except Exception as exc:
            logger.warning("Failed to load kb_settings for %s: %s", kb_name, exc)

    # Prepend the per-KB persona (if any) to the synthesis system prompt.
    # Personas like "You are a senior security engineer writing for a SOC
    # team" let different KBs synthesize in different voices without
    # touching the global SYNTHESIS_SYSTEM_PROMPT.
    effective_system_prompt = SYNTHESIS_SYSTEM_PROMPT
    if persona:
        effective_system_prompt = f"{persona.strip()}\n\n{SYNTHESIS_SYSTEM_PROMPT}"

    # Detect topic type and get adaptive template
    from app.topic_classifier import classify_topic, get_article_template
    topic_type = classify_topic(topic, all_results)
    article_template = get_article_template(topic_type)
    logger.info("Topic classified as '%s' for synthesis", topic_type)

    synthesis_prompt = f"""Create a well-structured research document on the following topic.

Topic: {topic}
Topic Type: {topic_type}

You have {len(all_results)} search results from multiple rounds of agentic research (broad search, sub-topic deep dives, and verification searches). Sources are labeled by authority tier:
- Tier 1 (Authoritative): official docs, .gov, academic, major tech blogs. PRIORITIZE these.
- Tier 2 (Reputable): Stack Overflow, known blogs, reputable news. Use as supporting evidence.
- Tier 3 (General): other blogs, forums. Use cautiously, cross-reference when possible.

CRITICAL RULES:
- Only state facts that come from the provided sources. Do NOT fill in gaps with general knowledge or your training data.
- If a source is clearly about a different topic (wrong subject, spam, unrelated content), IGNORE it completely.
- If an obvious aspect of this topic isn't covered by any source, call it out in Limitations. Do NOT guess.
- Focus on what makes this topic interesting and useful, not exhaustive raw data.
- If you want to include a comparison table, every cell must be citable. Leave cells blank or write "not available" rather than guessing.
- Complete every section fully. If approaching token limits, close the current section cleanly.

TITLE: Write a clear, specific title for this article. Do NOT use the pattern "X: A Comprehensive Research Document." Use a natural title like "Townsville, Australia" or "Kubernetes RBAC Best Practices."

{article_template}

Search Results:
{sources_text}"""

    # Retry synthesis up to RESEARCH_MAX_RETRIES times on timeout
    from app.config import RESEARCH_MAX_RETRIES, RESEARCH_RETRY_DELAY
    draft: str | None = None
    for attempt in range(1, RESEARCH_MAX_RETRIES + 2):
        try:
            draft = await _chat(
                client,
                system_msg=effective_system_prompt,
                user_msg=synthesis_prompt,
                max_tokens=16000,
                temperature=0.2,
                model=synth_model,
                provider=synth_provider,
            )
            break
        except (httpx.TimeoutException, httpx.ReadTimeout):
            if attempt <= RESEARCH_MAX_RETRIES:
                logger.warning("Synthesis timeout (attempt %d/%d), retrying in %ds...",
                               attempt, RESEARCH_MAX_RETRIES + 1, RESEARCH_RETRY_DELAY)
                await asyncio.sleep(RESEARCH_RETRY_DELAY)
            else:
                raise

    if draft is None:
        # Should never reach here — the retry loop either returns a draft
        # or raises — but appease the type checker.
        raise RuntimeError("Synthesis returned no draft")

    # Self-citation lint — strip any [label](url) where the URL wasn't in
    # the source set the model was given. Hallucinated citations would
    # otherwise leak through into the published article.
    source_urls = [r.get("url", "") for r in all_results if r.get("url")]
    cleaned, stripped = _lint_citations(draft, source_urls)
    if stripped:
        logger.warning(
            "Synthesis: stripped %d hallucinated citation(s) — first few: %s",
            len(stripped), stripped[:3],
        )

    # Critique pass — verify the draft against the sources and append a
    # Confidence Notes section. Failures are logged and ignored so a
    # critique-pass error never blocks article publishing.
    critique_claims: list[dict] = []
    try:
        critique_claims = await _critique_draft(
            client, topic, cleaned, sources_text,
        )
    except Exception as exc:
        logger.warning("Critique pass crashed: %s", exc)

    if critique_claims:
        notes = _render_confidence_notes(critique_claims)
        if notes:
            cleaned = cleaned.rstrip() + notes

    return cleaned, critique_claims


# --- Query refinement -------------------------------------------------------

async def refine_query(topic: str) -> str:
    """Use LLM to fix spelling errors and improve search query.

    Returns the refined query, or the original topic if refinement fails.
    """
    has_llm = (LLM_PROVIDER == "bedrock") or MINIMAX_API_KEY
    if not has_llm:
        return topic

    try:
        result = await llm_chat(
            system_msg=(
                "You are a search query optimizer. Fix any spelling errors, expand abbreviations, "
                "and improve the query for web search. Return ONLY the corrected query text, nothing else. "
                "Do not add quotes or extra formatting. If the query is already correct, return it unchanged."
            ),
            user_msg=topic,
            max_tokens=100,
            temperature=0.0,
        )
        refined = result.strip().strip('"').strip("'")
        if refined and 3 <= len(refined) <= len(topic) * 3:
            if refined.lower() != topic.lower():
                logger.info("Query refined: '%s' -> '%s'", topic, refined)
            return refined
    except Exception as exc:
        logger.warning("Query refinement failed: %s", exc)

    return topic


# --- Main pipeline ----------------------------------------------------------

async def run_research(topic: str, job_id: int, kb_name: str = "personal") -> None:
    """Execute the full 3-round research pipeline for a topic.

    Updates the SQLite job record at each stage. On completion, writes the
    research output to disk and stores the synthesized content in the DB.

    ``kb_name`` is used purely for per-KB Serper budget accounting; it does
    not affect where the article is later merged (that's the caller's job).
    """
    _job_token = _current_job_id.set(job_id)
    _kb_token = _current_kb.set(kb_name)
    try:
        return await _run_research_impl(topic, job_id)
    finally:
        _current_job_id.reset(_job_token)
        _current_kb.reset(_kb_token)


async def _run_research_impl(topic: str, job_id: int) -> None:
    await db.update_job(job_id, status="searching_round_1")

    if not SERPER_API_KEY:
        await db.update_job(
            job_id, status="error",
            error="No search API key configured (SERPER_API_KEY required)",
        )
        return

    if LLM_PROVIDER == "minimax" and not MINIMAX_API_KEY:
        await db.update_job(job_id, status="error", error="MINIMAX_API_KEY not set")
        return

    # Refine query: fix spelling, expand abbreviations
    search_query = await refine_query(topic)

    all_results: list[dict] = []

    # ── Round 1: Broad search across ALL registered providers ────────────
    # Serper, Tavily, arXiv, HN, Wikipedia, etc. run in parallel.
    # Per-provider failures are non-fatal; the rest still contribute.
    try:
        async with httpx.AsyncClient(timeout=30) as client:
            round1_results = await _round1_search_all_providers(
                client, search_query, per_provider=5,
            )
            all_results.extend(round1_results)

        logger.info(
            "Research [%d] round 1: %d results for '%s' (query: '%s')",
            job_id, len(all_results), topic, search_query,
        )
    except httpx.HTTPStatusError as exc:
        await db.update_job(
            job_id, status="error",
            error=f"Search API HTTP {exc.response.status_code}: {exc.response.text[:200]}",
        )
        return
    except httpx.TimeoutException:
        await db.update_job(
            job_id, status="error", error="Search API request timed out (round 1)",
        )
        return
    except Exception as exc:
        await db.update_job(job_id, status="error", error=f"Search failed (round 1): {exc}")
        return

    if not all_results:
        await db.update_job(job_id, status="no_results", sources_count=0)
        return

    # Score and save round 1 sources
    for r in all_results:
        r["tier"] = _score_source_tier(r.get("url", ""))
    await db.save_sources(job_id, all_results, round_num=1)

    # ── Round 2: Sub-topic queries via Minimax ─────────────────────────────
    await db.update_job(job_id, status="searching_round_2")

    round1_summaries = "\n".join(
        f"- {r['title']}: {r['content'][:150]}"
        for r in all_results
        if r.get("content")
    )

    round2_results: list[dict] = []
    sub_questions: list[str] = []
    try:
        async with httpx.AsyncClient(timeout=60) as client:
            sub_questions = await _generate_sub_questions(client, topic, round1_summaries)
            logger.info(
                "Research [%d] round 2: generated %d sub-questions",
                job_id, len(sub_questions),
            )
    except Exception as exc:
        logger.warning("Research [%d] sub-question generation failed: %s", job_id, exc)

    if sub_questions:
        try:
            async with httpx.AsyncClient(timeout=30) as client:
                for sq in sub_questions:
                    try:
                        sq_results = await _search_serper(client, sq, num=4)
                        round2_results.extend(sq_results)
                    except Exception as exc:
                        logger.warning(
                            "Research [%d] sub-search failed for '%s': %s",
                            job_id, sq[:50], exc,
                        )
        except Exception as exc:
            logger.warning("Research [%d] round 2 search phase failed: %s", job_id, exc)

    for r in round2_results:
        r["tier"] = _score_source_tier(r.get("url", ""))
    all_results.extend(round2_results)
    await db.save_sources(job_id, round2_results, round_num=2)

    # ── Deduplication and scoring ──────────────────────────────────────────
    all_results = _deduplicate_results(all_results)
    for r in all_results:
        if "tier" not in r:
            r["tier"] = _score_source_tier(r.get("url", ""))
    all_results.sort(key=lambda r: r["tier"])

    logger.info("Research [%d] after dedup: %d unique results", job_id, len(all_results))

    # ── Round 3: Verification searches ─────────────────────────────────────
    await db.update_job(job_id, status="searching_round_3")

    round3_results: list[dict] = []
    verification_queries: list[str] = []
    try:
        async with httpx.AsyncClient(timeout=60) as client:
            top_sources = _format_sources_for_prompt(all_results[:15])
            verification_queries = await _generate_verification_queries(
                client, topic, top_sources,
            )
            logger.info(
                "Research [%d] round 3: %d verification queries",
                job_id, len(verification_queries),
            )
    except Exception as exc:
        logger.warning("Research [%d] claim extraction failed: %s", job_id, exc)

    if verification_queries:
        try:
            async with httpx.AsyncClient(timeout=30) as client:
                for vq in verification_queries:
                    try:
                        vq_results = await _search_serper(client, vq, num=3)
                        round3_results.extend(vq_results)
                    except Exception as exc:
                        logger.warning(
                            "Research [%d] verification search failed for '%s': %s",
                            job_id, vq[:50], exc,
                        )
        except Exception as exc:
            logger.warning("Research [%d] round 3 search phase failed: %s", job_id, exc)

    for r in round3_results:
        r["tier"] = _score_source_tier(r.get("url", ""))
    all_results.extend(round3_results)
    await db.save_sources(job_id, round3_results, round_num=3)

    # Re-deduplicate after round 3
    all_results = _deduplicate_results(all_results)
    for r in all_results:
        if "tier" not in r:
            r["tier"] = _score_source_tier(r.get("url", ""))
    all_results.sort(key=lambda r: r["tier"])

    # ── Round 4a: Download PDFs/ePubs found in search results ─────────────
    try:
        from app.browser import find_document_urls, download_documents

        doc_urls = find_document_urls(all_results)
        if doc_urls:
            await db.update_job(job_id, status="downloading_docs")
            docs = await download_documents(doc_urls[:3])  # max 3 documents
            for doc in docs:
                # Keep up to 8000 chars from docs; the formatter will
                # further cap based on the source's tier.
                all_results.append({
                    "title": doc["title"],
                    "content": doc["content"][:8000],
                    "url": doc["url"],
                    "tier": _score_source_tier(doc["url"]),
                    "source": doc.get("source", "downloaded"),
                })
            logger.info(
                "Research [%d] documents: downloaded %d/%d",
                job_id, len(docs), len(doc_urls),
            )
            await db.save_sources(job_id, [
                {"title": d["title"], "content": d["content"][:500], "url": d["url"]}
                for d in docs
            ], round_num=4)
    except Exception as exc:
        logger.warning("Research [%d] document download failed: %s", job_id, exc)

    # ── Round 4b: Deep page reading (agent-browser + httpx fallback) ─────
    # Read full page content from the top Tier 1/2 URLs found in search
    await db.update_job(job_id, status="reading_pages")
    try:
        from app.browser import read_pages_smart

        # Pick top URLs from Tier 1 and Tier 2 sources
        # Tier values are integers (1, 2, 3) from _score_source_tier.
        urls_to_read = []
        seen_urls = set()
        for r in all_results:
            url = r.get("url", "")
            if (
                url
                and url.startswith("http")
                and r.get("tier") in (1, 2)
                and url not in seen_urls
            ):
                urls_to_read.append(url)
                seen_urls.add(url)
            if len(urls_to_read) >= 5:
                break

        if urls_to_read:
            pages = await read_pages_smart(urls_to_read, max_concurrent=3)
            for page in pages:
                # Keep up to 8000 chars from deep-reads; the formatter
                # will cap based on the resolved source tier.
                all_results.append({
                    "title": page["title"],
                    "content": page["content"][:8000],
                    "url": page["url"],
                    "tier": _score_source_tier(page["url"]),
                    "source": page.get("source", "jina_reader"),
                })
            logger.info(
                "Research [%d] deep reading: %d/%d pages read",
                job_id, len(pages), len(urls_to_read),
            )
            await db.save_sources(job_id, [
                {"title": p["title"], "content": p["content"][:500], "url": p["url"]}
                for p in pages
            ], round_num=4)
        else:
            logger.info("Research [%d] no Tier 1/2 URLs to read", job_id)
    except Exception as exc:
        logger.warning("Research [%d] deep page reading failed: %s", job_id, exc)

    # ── Synthesis ──────────────────────────────────────────────────────────
    await db.update_job(job_id, status="synthesizing", sources_count=len(all_results))

    try:
        async with httpx.AsyncClient(timeout=240) as client:
            content, critique_claims = await _synthesize(client, topic, all_results)
    except httpx.HTTPStatusError as exc:
        await db.update_job(
            job_id, status="error",
            error=f"Minimax API HTTP {exc.response.status_code}: {exc.response.text[:200]}",
        )
        return
    except httpx.TimeoutException:
        await db.update_job(
            job_id, status="error",
            error="Minimax API request timed out during synthesis",
        )
        return
    except Exception as exc:
        await db.update_job(job_id, status="error", error=f"Synthesis failed: {exc}")
        return

    # ── Write output ───────────────────────────────────────────────────────
    await db.update_job(job_id, status="writing")

    today = datetime.now(timezone.utc).strftime("%Y-%m-%d")
    safe_topic = "".join(c if c.isalnum() or c in " -" else "_" for c in topic)[:80]
    filename = f"research-{today}-{safe_topic.replace(' ', '-')}.md"

    try:
        from app import storage
        from app.config import RESEARCH_KB
        report = f"# Research: {topic}\n\n*{today}*\n\n{content}\n"
        storage.write_text(RESEARCH_KB, filename, report)
    except Exception as exc:
        await db.update_job(job_id, status="error", error=f"Failed to write output: {exc}")
        return

    await db.update_job(
        job_id,
        status="complete",
        completed_at=datetime.now(timezone.utc).isoformat(),
        sources_count=len(all_results),
        word_count=len(content.split()),
        content=content,
        claims_json=(json.dumps(critique_claims) if critique_claims else None),
    )
    logger.info(
        "Research [%d] complete: %d sources, %d words, %d critique claims",
        job_id, len(all_results), len(content.split()), len(critique_claims),
    )


